#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
{
	char	*newstr;
	size_t	tlen;
	size_t	i;
	size_t	j;

	j = 0;
	i = 0;
	tlen = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	newstr = (char *)malloc(tlen + 1);
	if (newstr == NULL)
		return (NULL);
	while (s1[i])
	{
		newstr[i] = s1[i];
		i++;
	}
	while (s2[j])
		newstr[i++] = s2[j++];
	newstr[i] = '\0';
	return (newstr);
}

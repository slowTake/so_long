#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dst;
	int		len;

	if (!src)
		return (NULL);
	len = ft_strlen((char *)src);
	dst = malloc(len + 1);
	if (!dst)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
